/**
 * Returns the sum of two numbers.
 * @param {number} a - The first number.
 * @param {number} b - The second number.
 * @returns {number} - The sum of the two numbers.
   function getSum(a: number, b: number): number;
 */

function getSum(a, b) {
  return a + b;
}

const result = getSum(2, 3); //5
console.log(result);
